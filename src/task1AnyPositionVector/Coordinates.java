package task1AnyPositionVector;

public class Coordinates {
    double x;
    double y;
    double z;

    public Coordinates(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Coordinates(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
